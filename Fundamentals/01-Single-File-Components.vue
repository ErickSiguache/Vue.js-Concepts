<!--
    En Vue.js, los "single file components" (componentes de un solo archivo)
    son una característica que permite definir componentes Vue en un solo
    archivo. Tradicionalmente, las aplicaciones Vue.js se desarrollan
    mediante componentes que se dividen en tres partes: la plantilla (HTML),
    el script (JavaScript) y los estilos (CSS). Con los "single file
    components", estas tres partes se combinan en un solo archivo con la
    extensión .vue.

    Ejemplo:
-->

<template>
    <!-- La plantilla (HTML) del componente -->
</template>

<script>
    // La lógica del componente (JavaScript)
</script>

<style>
    /* Los estilos del componente (CSS) */
</style>

<!--
    NOTA:

    Cada uno de estos elementos es independiente, lo que significa que puedes
    crear un archivo que contenga solo <script> o solo <template>.
-->
